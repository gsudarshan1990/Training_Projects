"""
This is example of the ordered dictionary
"""

from collections import OrderedDict

ordered_dictionary1 = OrderedDict()
ordered_dictionary1['a'] = 1
ordered_dictionary1['b'] = 2
ordered_dictionary1['c'] = 3
ordered_dictionary1['d'] = 4
ordered_dictionary1['e'] = 5
print(ordered_dictionary1)

ordered_dictionary2 = OrderedDict()
ordered_dictionary2['a'] = 'A'
ordered_dictionary2['b'] = 'B'
ordered_dictionary2['c'] = 'C'
ordered_dictionary2['d'] = 'D'
ordered_dictionary2['e'] = 'E'
print(ordered_dictionary2)